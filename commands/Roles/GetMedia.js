module.exports = {
    commands: ['GetMedia'],
    minArgs: 1,
    expectedArgs: "<@ User>",
    requiredRoles: ['Head Media'],
    callback: (message, arguments) => {
        const targetUser = message.mentions.users.first();
        if (!targetUser) {
            message.reply('لطفا یک یوزر را منشن کنید');
            return;
        }

        arguments.shift();

        const { guild } = message;

        const member = guild.members.cache.get(targetUser.id);

        if (member.roles.cache.get('868117308969738260')) {
            member.roles.remove('868117308969738260');
            message.reply(`رول از یوزر گرفته شد`);
        } else {
            message.reply(`یوزر تعریف شده رول داده شده را ندارد`);
        }
    },
};